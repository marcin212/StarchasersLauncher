package pl.starchasers.launcher.utils.json;


public class MinecraftVersions {
	private String id;
	private String time;
	private String releaseTime;
	private String type;
	
	public String getId(){
		return this.id;
	}
	public String getTime(){
		return this.time;
	}
	public String getReleaseTime(){
		return this.releaseTime;
	}
	public String getType(){
		return this.type;
	}
}
